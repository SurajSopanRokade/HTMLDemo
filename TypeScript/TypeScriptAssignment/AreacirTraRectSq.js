function circle(radius) {
    console.log("Area of Circle : " + (3.14 * radius * radius));
}
function square(side) {
    console.log("Area of square : " + (side * side));
}
function rectangle(length, breadth) {
    console.log("Area of rect : " + (length * breadth));
}
function triangle(height, base) {
    console.log("Area of trangle : " + (1 / 2 * height * base));
}
circle(3);
square(4);
rectangle(5, 6);
triangle(4, 7);
